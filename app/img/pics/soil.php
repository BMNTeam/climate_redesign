					В Ставропольском крае вследствие изменения климата и нарушения гидрологического режима территории увеличивается площадь переувлажненных земель: с 1985 по 2000 год на 82 %. Только за 5 лет количество дефлируемых земель в сельскохозяйственных угодьях возросло на 19,7 %, эродированных – 1,3 %, совместного проявления ветровой и водной эрозии – 9,9 %.

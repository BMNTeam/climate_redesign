					<p>
					В то же время геологическое строение территории края и его рельеф определяют широкое развитие и таких негативных явлений для сельского хозяйства, как засоление и осолонцевание почв, дефляция песчаных массивов и почв в местных ветровых коридорах, плоскостная и линейная эрозия почв.
					</p>
					<span class="header2"><center>Почвы</center></span>
					<p>
					Основными почвообразующими породами на Ставрополье являются четвертичные отложения. Большая часть его равнинной территории представлена лессовидными карбонатными суглинками, на северо-востоке края сменяющимися пылеватыми засоленными суглинками, а на востоке – песками древнеэлювиального происхождения, образовавшимися в результате деятельности древних рек Терека и Кумы. На востоке и северо-востоке края широко распространены четвертичные засоленные морские и озерные, аллювиальные и аллювиально-морские отложения (глины, суглинки, супеси) (рис. 3).
					</p>
					<img src="pics/mother_soil.jpg" width="880">
							<span class="italic_text"><center>
					Рисунок 3 – Карта почвообразующих пород Ставропольского края ("СтавропольНИИгипрозем", 1977)
					</center></span>
					
					<p>
					Толща лессовидных суглинков достигает 35–40 метров, уменьшаясь до 20 метров и менее в различных частях равнинной территории края. Они отличаются крайней разнородностью как по условиям залегания, так по составу и свойствам, содержанию кремнезема, окислов алюминия и железа. Эти породы характеризуются наличием значительного количества фосфора и калия, и на них залегают наиболее плодородные почвы края – черноземы и каштановые почвы.
					</p>
					<p>
					На Ставропольской возвышенности почвы сформированы на элювиально-делювиальных отложениях, элювии плотных пород – известняка, песчаника и гипсоносных глин и мергелей.
					</p>
					<p>
					В Янкульской и Сенгилеевской котловинах почвообразующими породами являются соленосные третичные майкопские и сарматские глины. Майкопские засоленные глины содержат незначительное количество карбонатов (3,6 %), а в водной вытяжке отмечено преобладание натрия над кальцием и магнием и высокий плотный остаток. На них сформировались черноземы слитые солонцеватые.
					</p>
					<p>
					На территории Предкавказья выделяются два преобладающих типа почв, интенсивно используемых в земледелии: черноземы и каштановые почвы. Первые приурочены к его западной части и занимают около 38 % территории края, вторые – к восточной на площади 43 %. Остальная территория края представлена солонцами, солончаками, песками, пойменными, луговыми и другими почвами.
					</p>
					<p>
					Среди черноземов выделяются выщелоченные, типичные, обыкновенные, южные, а среди каштановых почв – темно-каштановые, каштановые, светло-каштановые и их солонцеватые разности.
					</p>
					<p>
					Выщелоченные и типичные черноземы приурочены к предгорьям и центральной части Ставропольской возвышенности и значительного распространения не получили (1,0 %). Черноземы южные тянутся неширокой полосой (менее 70 км) по границе между зонами черноземов и каштановых почв, занимая 10 % территории. Преобладают в крае черноземы обыкновенные (20,8 %). Слитые солонцеватые черноземы слагают территорию Предкавказской впадины по линии Невинномысск – Курсавка – Минеральные Воды, Янкульскую и Сенгилеевскую котловины. Площадь их, не считая солонцов, составляет 7 % территория края.
					</p>
					<p>
					В географии каштановых почв прослеживается следующая закономерность: к югу, юго-востоку от линии Курская – Зеленокумск – Благодарный доминируют темно-каштановые почвы (20 %), целиком слагая некоторые административные районы. По мере продвижения к северу и северо-западу они постепенно сменяются каштановыми почвами (17 %), те в свою очередь – солонцеватыми с нарастанием доли участия солонцов, которые на верхних террасах Маныча уже становятся доминантами почвенных комплексов. Светло-каштановые почвы (6 %) занимают самостоятельный ареал и строго приурочены к оконечностям Прикаспийской низменности. Здесь почвенный покров исключительно комплексный: с солонцами, солончаками, песками.
					</p>
					<p>
					Каштановые почвы формируются под влиянием изреженной сухостепной растительности со степенью проективного покрытия не выше 50–70 %. Объем биомассы значительно ниже, чем в степях, под которыми формируются черноземы, т. е. дерновый процесс протекает в ослабленной форме. Запасы гумуса в гумусовом слое не превышают 90–120 т/га. Слабое выщелачивание и миграция солей обусловлены непромывным типом водного режима, поэтому все простые соли замкнуты в пределах почвы и коры выветривания и имеют тенденцию постоянного накопления.
					</p>
					<p>
					Солонцы и солонцовые почвы наиболее распространены в зоне каштановых почв, а в зоне черноземов они занимают самостоятельный ареал в пределах Предкавказской депрессии. Их общая площадь составляет около 1,5 млн га. Солонцы являются интразональными почвами, чаще всего встречаются в комплексах с другими почвами, занимая в них площадь от 5 до 50 %.
					</p>
					<p>
					В морфологическом отношении каштановые и черноземные солонцы имеют иллювиальный горизонт, обладающий неблагоприятными физическими свойствами: глыбистой структурой, высокой плотностью, низкой пористостью. Эти свойства связаны с наличием в иллювиальном горизонте солонцов обменного натрия в количестве более 15–20 % от емкости поглощения. Почвы с количеством обменного натрия от 3–5 до 15–20 % относят к солонцеватым.
					</p>
					<p>
					В каштановых солонцах на глубине 35–40 см в подсолонцовом горизонте имеются свободные карбонаты в виде белоглазки, в черноземных солонцах белоглазка залегает глубже 50–70 см. Солонцы каштановые преимущественно среднесуглинистые, черноземные – тяжелосуглинистые и глинистые (рис. 4). Встречаются черноземные солонцы (малонатриевые), которые имеют в почвенном поглощающем комплексе мало натрия и много магния. Имея комплекс неблагоприятных физических, физико-механических и водно-физических свойств, солонцы довольно богаты элементами питания растений, поэтому на них эффективно выращивание солеустойчивых сельскохозяйственных культур и трав, особенно злаковых, или использование их под пастбища.
					</p>

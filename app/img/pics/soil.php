					Все сельскохозяйственные земли используются по назначению, в том числе и пашня. Залежей на всей территории края очень мало – 14,0 тыс. га, или 0,23 % от площади земель сельскохозяйственного назначения (табл. 7). По данным управления Росреестра по Ставропольскому краю на 1 января 2012 года, в составе земель сельскохозяйственного назначения 70,17 % находится в частной собственности граждан (63,87 %) и юридических лиц (6,30 %), остальные 29,83 % – в государственной собственности.
					</p>

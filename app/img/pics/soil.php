					</p>
					<p>
					Существующий каркас полезащитных лесных полос (94,4 тыс. га) постепенно утрачивает свои мелиоративные функции, т. к. 56 % лесополос (посадки 1948–1953 гг.) уже достигли своего предельного возраста, что требует их перезакладки, 18 % нуждаются в срочных рубках ухода и только 26 % выполняют свои функции, хотя здесь необходимо проводить уходные мероприятия.
					</p>
					<p>
					Сохранить и восстановить утрачиваемое плодородие земель в настоящее время можно только переходом на адаптивно-ландшафтное земледелие с помощью землеустройства, учитывающего состояние земельных ресурсов, обеспечивающего планирование и организацию их рационального использования и охрану, которое должно осуществляться как первоочередное мероприятие. В условиях рынка разрабатываемые проекты землеустройства, во-первых, должны опираться на эколого-ландшафтные принципы, во-вторых, быть инвестиционными, определяющими не только экологическую безопасность территории хозяйств, но и пути получения экономической выгоды использования земли.
					</p>
					<p>
					Для сравнения территорий (хозяйств, районов, ландшафтов) по видам деградационных процессов и степени их развития, а также планирования очередности разработки землеустроительных проектов в Ставропольском НИИСХ разработана интегральная количественная оценка деградационных процессов, опирающаяся не только на площадь поражения, но и интенсивность развития процессов. Произведение площади поражения (%) на интенсивность поражения (долю снижения плодородия) служит обобщающим баллом вредоносности, а сумма баллов всех деградационных процессов позволяет оценить территории по степени поражения деградациями.

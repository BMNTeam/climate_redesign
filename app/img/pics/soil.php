					<p>
					Производителями сельскохозяйственной продукции используется 6097 тыс. га, в том числе предприятиями, организациями 4406 тыс. га, гражданами – 1691 тыс. га, в том числе фермерами – 849,9 тыс. га. Среди всех земель, на которых производится сельскохозяйственная продукция предприятиями, организациями, хозяйствами и обществами, доминируют земли собственников земельных долей: 2544 тыс. га, или 57,7 %. Более четверти площадей 1231,7 тыс. га (28 %) составляют земли, находящиеся в государственной собственности. В крупных предприятиях 25,0 % земель используется на праве аренды или пользования.
					</p>
					<p>
					Общая площадь земель фермерских хозяйств составляет 849,9 тыс. га, или 13,9 % всех земель, из них 37,5 % – на праве собственности, а 61,5 % – аренды и пользования. Пахотные земли края находятся в зоне риска: 46,6 % – эрозионно-, а 87,2 % – дефляционно опасны. Большой процент арендуемых земель в пользовании крупных и мелких товаропроизводителей является неблагоприятным фактором, не способствующим сохранению и повышению плодородия земель.
					</p>
					
					<p>
					Орошаемые земли Ставропольского края характеризуются неудовлетворительным состоянием. По сравнению с 1990 годом (404430 га) к 2012 году площадь орошаемых земель сократилась на треть (31,7 %) и составляет 276373 га. Ухудшилось и их мелиоративное состояние в результате вторичного засоления и подтопления, осолонцевания, слитизации, дегумификации на площади 34,5 тыс. га, или 12,5 % площади орошения.
					</p>
					
					<p>
					Ухудшилось техническое состояние мелиоративных систем: физический износ водомелиоративного комплекса составляет 70–75 %. Хронически недостаточное финансирование комплекса (в последние 15 лет на ремонт гидротехнических сооружений отпускается лишь 10–15 % средств от необходимого уровня) отрицательно влияет на его безопасность. Площадь оросительной сети, требующая реконструкции и ремонта, составляет в крае 87,6 тыс. га, или 31,7 %.
					</p>
					<p>
					В структуре земель края 27,6 % занимают естественные кормовые угодья, в том числе 1584,5 тыс. га пастбища и 102,3 тыс. га сенокосы. Размещены они по территории края неравномерно (рис. 16), занимая неудобные для распашки склоновые земли, а также низкобонитетные почвы, осложненные процессами засоления (46,5 %) и осолонцевания (27,4 %), нарушенные водной (23,0 %) и ветровой эрозией (15,4 %). Их низкая продуктивность обеспечивает имеющееся поголовье скота пастбищным кормом всего на 53 % от потребности. Учитывая, что природные угодья способствуют экологической устойчивости всех агроландшафтов края, остро стоит вопрос об улучшении их состояния.
					</p>
					<p>
					В системе мер по мелиорации сельскохозяйственных земель значительное место отводится полезащитным лесополосам, которые являются ресурсом, сберегающим плодородие земель и повышающим их продуктивность. Защитная лесистость пахотных угодий края составляет 2,4 %. Однако площадь полезащитных лесных насаждений с каждым годом сокращается. В хорошем состоянии находится только 29,7 % лесных насаждений, остальные нуждаются в перезакладке, реконструкции и рубках ухода. Долговременная бесхозность лесных полос, необходимость крупных капиталовложений по уходу за ними, дороговизна межевания и высокий налог на землю, которую они занимают, делают приватизацию лесных насаждений крупными и мелкими товаропроизводителями крайне непривлекательной. Поэтому большинство лесных насаждений варварски уничтожается и теряет свои мелиоративные функции.
					</p>
					<p>
					Улучшение состояния земельно-ресурсного потенциала требует и упорядочения земельных отношений в системе современных аграрных преобразований. Так, до настоящего времени не решены проблемы нормативно-правового регулирования землепользования в предприятиях различных форм собственности и хозяйствования субъектов округа.
					</p>
					<p>
					Главными причинами нерешенности или нерешаемости изложенных проблем использования земельных ресурсов являются следующие:
					</p>
					<ul>
						<li>-несовершенство нормативно-правовой, законодательной базы и, в частности, «Земельного кодекса РФ», в котором отсутствуют четкие нормы и регламенты использования сельскохозяйственных земель землевладельцами и арендаторами. Не работает на сохранение и повышение плодородия почв закон «Об обеспечении плодородия земель сельскохозяйственного назначения»;</li>
						<li>-отнесение земельных ресурсов в ведение Росреестра снизило их статус до уровня объекта недвижимости, за сохранность которого по сути дела никто не отвечает;</li>
						<li>-отсутствие единой государственной системы управления земельными ресурсами: сегодня они находятся в ведении не менее 8 министерств;</li>
						<li>-развал системы институтов Гипрозема, которые через землеустройство, его схемы и проекты определяли стратегию рационального использования земли;</li>
						<li>-принятие множества законодательных актов усложнило саму процедуру их правового оформления как сельхозтоваропроизводителями, так и государственными структурами, которые имеют на сегодняшний день земли в доверительном управлении.</li>
				
					</ul>
									
					

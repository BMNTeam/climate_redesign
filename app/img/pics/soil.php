				<tr>
					<td align="center" valign="top">
								
					<p>
					В геологическом строении территории Ставропольского края принимают участие породы от древнейших палеозойских до четвертичных отложений. Гряда Пастбищного хребта сложена из известняков и песчано-глинистых отложений мелового периода – мезозоя (рис. 1).
					</p>
					<p>
					Фундамент Ставропольской возвышенности палеозойского возраста залегает в зоне Ставропольских высот на глубине около 1500 м, в районе Прикаспийской низменности – 3000 м. На этом фундаменте расположена толща осадочных пород, из которых наибольшее место занимают кайнозойские (палеоген-неогеновые) морские отложения.
					</p>
					<p>
					На участках предгорий и в нижних частях Ставропольской возвышенности наиболее распространены фораминифированные мергели и палеогеновые майкопские гипсоносные глины, полоса которых проходит по Предкавказской депрессии от Минеральных Вод к Армавиру с образованием значительных массивов в Янкульской и Сенгилеевской котловинах.
					</p>
					<p>
					Солонцы и солонцеватые почвы, сформированные на майкопских глинах в Янкульской и Сенгилеевской котловинах, на части территории Александровского и Минераловодского районов наиболее повреждены линейным размывам, что крайне осложняет закрепление растущих оврагов древесно-кустарниковой растительностью.
					</p>
					<img src="pics/geology.jpg" width="880">
					<span class="italic_text"><center>
					Рисунок 1 - Геологическая карта Ставропольского края (Паффенгольц и Голубятников, 1958) 
					</center></span>
					<p>
					Плакоры Ставропольской возвышенности бронированы слоями известняков и песчаников, под которыми лежат сарматские пески и глины. Крутые склоны останцовых плато, сложенных песками и глинами, способствуют развитию оползневых процессов и оврагообразованию. В крае насчитывается 9,3 тыс. га оползней и 46,5 тыс. га оползневых земель. В наибольшей степени оползневые процессы развиты на Ставропольской возвышенности, Воровсколесских высотах, правобережье Кубани и Кумы.
					</p>
					<p>
					По восточной, западной и северной окраинам Ставропольской возвышенности выделяется узкая полоса ракушечников, конгломератов и других отложений плиоценовых морей палеогена и неогена. Свита изолированных магматических гор на Минераловодской равнине сложена липаритотрахитовыми магматическими породами.
					</p>
					<p>
					Территория края имеет резко выраженную неоднородность рельефа, характеризующуюся перепадом высот от 5 до 1542 м над уровнем моря (гора Джинал). Его формирование проходило как под воздействием тектонических, так и денудационно-аккумулятивных процессов, а также деятельности Кубани, Терека, Кумы, Калауса и Егорлыка – самых крупных рек Ставрополья. Остальные относятся к категории малых рек, которых на Ставрополье насчитывается до 220.
					</p>
					<p>
					В границах края горная система Большого Кавказа представлена Пастбищным хребтом. Пастбищный хребет разделен речными долинами на несколько более мелких: Боргустан (с высотами до 1209 м), Дарьинский (до 1463 м), Джинальский (до 1541 м) и Кабардинский (до 1605 м в пределах края).
					</p>
					<p>
					Предгорные наклонные равнины: Прикубанская, Минераловодская, Кабардинская и Кубано-Суркульская депрессия отделяют Сычевско-Воровсколесский останцовый массив, высшая точка которого достигает высоты 852 метров, от Ставропольской возвышенности. На Минераловодской равнине расположен своеобразный Пятигорский вулканический район, состоящий из 17 останцовых магматических гор.
					</p>
					<p>
					Северную часть территории края занимает Предкавказская равнина, центральная часть которой представлена Ставропольской возвышенностью, которая долинами рек разделена на отдельные останцовые участки (Ставропольские и Прикалаусские высоты, горы Недреманная, Бударка, Баба и др.).
					</p>
					<p>
					Гора Стрижамент (832 м) является не только самой высокой точкой Ставропольской возвышенности, но и всей Русской равнины.
					</p>
					<p>
					Ставропольские высоты переходят на юго-востоке в более низкие Бешпагирские высоты. Долина р. Калаус отделяет Ставропольские от Прикалаусских высот, простирающихся вдоль Калауса с севера на юг на 140км с высотными отметками от 300 метров (г. Куцай) на севере до 688 метров на юге (г. Брык). В пределах Ставропольской возвышенности находятся две значительные котловины – Сенгилеевская и Янкульская с отметками от 200 до 230 метров над уровнем моря. Ставропольская возвышенность на северо-западе, северо-востоке и востоке постепенно переходит в Азово-Кубанскую низменность, Кумо-Манычскую впадину и Терско-Кумскую низменность.
					</p>
					<p>
					Кумо-Манычская впадина расположена в депрессии между Ставропольской и Ергенинской возвышенностями, общей протяженностью 270 км в границах края. Она является остатком протока, связывающего Азовское и Каспийское моря. В настоящее время эта впадина занята большим количеством озер, речных террас и сухих русел.
					</p>
					<p>
					Терско-Кумская низменность, простирающаяся на востоке края, является частью Прикаспийской низменности. Высотные отметки рельефа понижаются с юго-запада от Прикумской возвышенности (300–320 м) на северо-восток к Терско-Кумской низменности (10–100 метров). Равнины и низменности восточной части края нарушаются песчаными массивами, представленными комплексом грядовых, бугристых и барханных форм рельефа.
					</p>
					<p>
					Своеобразный рельеф края образует «ветровые коридоры» (Невинномысский, Армавирский, Сенгилеевский, Дубовский), а большие высотные отметки юго-западных окраин Ставропольской возвышенности создают эффект «падающего» ветра, что определяет высокую дефляцию не только в западных районах края, но и в соседних северо-восточных районах Краснодарского края.
					</p>
